export const getDashboard = async (req: Request, res: Response) => {
  try {
    const studentId = req.user?.userId;
    const submissions = await Submission.find({ studentId }).populate('examId', 'title totalMarks passingMarks');

    const examsTaken = submissions.length;
    const passedExams = submissions.filter(s => s.isPassed).length;
    const failedExams = examsTaken - passedExams;

    let totalScore = 0;
    submissions.forEach(s => totalScore += s.score);
    const averageScore = examsTaken > 0 ? (totalScore / examsTaken) : 0;

    // Heatmap data could be generated by analyzing subject performance
    // For now we just return basic stats
    res.json({ examsTaken, passedExams, failedExams, averageScore, submissions });
  } catch (error) {
    res.status(500).json({ message: 'Server error' });
  }
};


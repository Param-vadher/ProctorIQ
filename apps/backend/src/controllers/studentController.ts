// EXAM LOBBY
// ========================
export const getExamLobby = async (req: Request, res: Response) => {
  try {
    const now = new Date();

    // Categorize exams based on windowStart and windowEnd, AND student assignment
    const exams = await Exam.find({
      isActive: true,
      assignedStudents: req.user?.userId
    }).populate('subjectId', 'name');

    const active = exams.filter(e => e.windowStart <= now && e.windowEnd >= now);
    const upcoming = exams.filter(e => e.windowStart > now);
    const expired = exams.filter(e => e.windowEnd < now);

    res.json({ active, upcoming, expired });
  } catch (error) {
    res.status(500).json({ message: 'Server error' });
  }
};

